#include <stddef.h>

size_t	ft_strlen(const char *str)
{
	size_t	i;

	i = 0;
	while (str[i])
		i++;
	return (i);
}
/*
 ft_strlen keep incrementing in the string and stop at the '\0'
 then at the end return an integer i that is the length of the string
*/